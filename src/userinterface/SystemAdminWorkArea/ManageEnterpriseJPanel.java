    private void submitJBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJBtnActionPerformed

        Network network = (Network) netwrkJComboBox.getSelectedItem();
        Enterprise.EntType type = (Enterprise.EntType) entTypeJComboBox.getSelectedItem();

        if (network == null || type == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        } else {
            String enterpriseName = nameJTextField.getText();
            List<Enterprise> enterprises = network.getEntDir().getEntList();
            List<String> names = new ArrayList<>();
            for (Enterprise enterprise : enterprises) {
                names.add(enterprise.getName());
            }
            if (names.contains(enterpriseName)) {
                JOptionPane.showMessageDialog(null, "Enterprise field Altready exists!!", "Warning!", JOptionPane.WARNING_MESSAGE);
                return;
            } else {
                if (enterpriseName.equals("") || enterpriseName == null) {
                    JOptionPane.showMessageDialog(null, "Name field cannot be empty!");
                    return;
                } else {
                    Enterprise enterprise = network.getEntDir().crtAndAddEnt(enterpriseName, type);

                    popTbl();
                    JOptionPane.showMessageDialog(null, "Enterprise field Added Successfully!!");
                    nameJTextField.setText("");
                }
            }
        }

    }//GEN-LAST:event_submitJBtnActionPerformed

